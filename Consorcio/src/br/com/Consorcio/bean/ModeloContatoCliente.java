package br.com.Consorcio.bean;

public class ModeloContatoCliente {
	
	private int celularUmCliente;
	private int celularDoisCliente;
	private int celularTresCliente;
	private int telefoneUmCliente;
	private int telefoneDoisCliente;
	private String emailCliente;
	
	public int getCelularUmCliente() {
		return celularUmCliente;
	}
	public void setCelularUmCliente(int celularUmCliente) {
		this.celularUmCliente = celularUmCliente;
	}
	public int getCelularDoisCliente() {
		return celularDoisCliente;
	}
	public void setCelularDoisCliente(int celularDoisCliente) {
		this.celularDoisCliente = celularDoisCliente;
	}
	public int getCelularTresCliente() {
		return celularTresCliente;
	}
	public void setCelularTresCliente(int celularTresCliente) {
		this.celularTresCliente = celularTresCliente;
	}
	public int getTelefoneUmCliente() {
		return telefoneUmCliente;
	}
	public void setTelefoneUmCliente(int telefoneUmCliente) {
		this.telefoneUmCliente = telefoneUmCliente;
	}
	public int getTelefoneDoisCliente() {
		return telefoneDoisCliente;
	}
	public void setTelefoneDoisCliente(int telefoneDoisCliente) {
		this.telefoneDoisCliente = telefoneDoisCliente;
	}
	public String getEmailCliente() {
		return emailCliente;
	}
	public void setEmailCliente(String emailCliente) {
		this.emailCliente = emailCliente;
	}
}
